package Lab;

public class CashArray {
    String name;
    int x;
    int y;
    String color;

    public void setName(String name) {
        this.name = name;
    }


    public String getName() {

        return name;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getColor() {

        return color;
    }
}
